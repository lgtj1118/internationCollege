package com.tjpu.bean;

public class InfoBean {
	private String num;
	private String courseName;
	private int beginWeek;
	private int endWeek;
	private float credits;
	private String courseType;
	private String checkway;
	private String classes;
	private String teacher;
	private String teacherid;
	private Integer stuNum;
	private Integer havestuNum;
	
	public InfoBean (String num, String courseName, int beginWeek, int endWeek, float credits, String character, String checkway, String teacher, String classes) {
		this.num = num;
		this.courseName = courseName;
		this.beginWeek = beginWeek;
		this.endWeek = endWeek;
		this.credits = credits;
		this.setCourseType(character);
		this.checkway = checkway;
		this.teacher = teacher;
		this.classes =classes;
			
	}
	public InfoBean(){}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public int getBeginWeek() {
		return beginWeek;
	}
	public void setBeginWeek(int beginWeek) {
		this.beginWeek = beginWeek;
	}
	public int getEndWeek() {
		return endWeek;
	}
	public void setEndWeek(int endWeek) {
		this.endWeek = endWeek;
	}
	public float getCredits() {
		return credits;
	}
	public void setCredits(float credits) {
		this.credits = credits;
	}
	public String getCheckway() {
		return checkway;
	}
	public void setCheckway(String checkway) {
		this.checkway = checkway;
	}
	public String getTeacher() {
		return teacher;
	}
	public void setTeacher(String teacher) {
		this.teacher = teacher;
	}
	public String getClasses() {
		return classes;
	}
	public void setClasses(String classes) {
		this.classes = classes;
	}
	public String getCourseType() {
		return courseType;
	}
	public void setCourseType(String courseType) {
		this.courseType = courseType;
	}
	public Integer getStuNum() {
		return stuNum;
	}
	public void setStuNum(Integer stuNum) {
		this.stuNum = stuNum;
	}
	public Integer getHavestuNum() {
		return havestuNum;
	}
	public void setHavestuNum(Integer havestuNum) {
		this.havestuNum = havestuNum;
	}
	public String getTeacherid() {
		return teacherid;
	}
	public void setTeacherid(String teacherid) {
		this.teacherid = teacherid;
	}
}